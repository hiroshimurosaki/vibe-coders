#include <bits/stdc++.h>

using namespace std;

int main(){

    int n;

    cin >> n;

    int pos2 = 1, count=0;

    do{
        if(pos2&1){
            pos2 = ceil(n/2.0) + pos2/2;
            count++;
        }else{
            pos2 = pos2/2;
            count++;
        }
    }while(pos2!= 1);

    cout << count << endl;
    
    return 0;
}
